function [ageGroup] = computeAgeGroup(age)

clc;

% definitions
groups = {...
    'Infant',...
    'Toddler',...
    'Child',...
    'Teenager',...
    'Adult',...
    };

ages = {...
    [0,1],...
    [1,2],...
    (3:12),...
    (13:19),...
    };

age = floor(age);

% script
if age >= 20
    ageGroup = groups{5};
    return;
end

for i = 1: length(ages)
    if any(age == ages{i}) == 1
        ageGroup = groups{i};
        return;
    end
end
end
